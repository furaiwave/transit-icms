/**
 * Єдине джерело правди для числових параметрів моделей обробки.
 * Таблиця 2.1 звіту будується з цього об'єкта, тому будь-яка зміна
 * порогу чи сигми одразу відображається у результатах — розсинхрон неможливий.
 */
export const MODEL_PARAMS = {
  /** σ похибки GPS-приймача, м (визначає R фільтра Калмана) */
  gpsSigmaMeters: 15,
  /** Спектральна густина шуму процесу Q, град²/с */
  qProcess: 2e-10,
  /** Коефіцієнт згладжування α експоненційного середнього швидкості */
  emaAlpha: 0.3,
  /** Довжина вікна ковзної статистики швидкості, кадрів */
  rollingWindow: 30,
  /** Мінімум кадрів у вікні до вмикання z-детектора */
  minSamplesForZ: 12,
  /** Поріг z-оцінки для викиду швидкості */
  zThreshold: 3,
  /** Максимальний вік кадру, мс */
  staleMs: 60_000,
  /** Порогова неявна швидкість стрибка GPS, км/год */
  jumpImpliedKmh: 160,
  /** Мінімальна інновація, що вважається стрибком, м */
  jumpMinMeters: 120,
  /** Мінімум кадрів до вмикання детектора стрибків */
  minFramesForJump: 3,
  /** Допуск відхилення від осі маршруту, м */
  offRouteMeters: 200,
  /** Випередження при пошуку наступної зупинки, м */
  etaLookaheadMeters: 15,
  /** Нижня межа швидкості в ETA, м/с (щоб не ділити на ~0) */
  etaMinSpeedMs: 1.5,
} as const;

export type ModelParams = typeof MODEL_PARAMS;

/** Градусний еквівалент σ GPS: 1° широти ≈ 111 320 м. */
export const DEG_PER_METER = 1 / 111_320;
export const GPS_SIGMA_DEG = MODEL_PARAMS.gpsSigmaMeters * DEG_PER_METER;
/** R фільтра Калмана — дисперсія вимірювання у градусах². */
export const R_MEASURE = GPS_SIGMA_DEG * GPS_SIGMA_DEG;
